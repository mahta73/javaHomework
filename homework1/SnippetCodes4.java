public class SnippetCodes4 {
    public static void main(String[] args) {
        int a = 10;
        int b = 128;
        
        // a = 10 =    0000000 00000000 00000000 00001010
        // &
        // a -1  = 9 = 0000000 00000000 00000000 00001001
        //             0000000 00000000 00000000 00001000 = (8)10

        // b = 128 =    00000000 00000000 00000000 10000000
        // &
        // b -1 = 127 = 00000000 00000000 00000000 01111111
        //              00000000 00000000 00000000 00000000 = (0)10

        int c = a & (a - 1);
        int d = b & (b - 1);
        System.out.println("The results are " + c + " and " + d);
    }
}