public class SnippetCodes3 {
    public static void main(String[] args) {
        int a = 192;
        int b = 12;

        // a = (192)10 = (00000000 00000000 00000000 11000000)
        // -a = 
        // 11111111 11111111 11111111 00111111
        // +1
        // 11111111 11111111 11111111 01000000


        // b = (12)10 = (00000000 00000000 00000000 00001100)
        // -b = 
        // 11111111 11111111 11111111 11110011
        // +1
        // 11111111 11111111 11111111 11110100
    
        int c = a & -a;

        // 00000000 00000000 00000000 11000000
        // &
        // 11111111 11111111 11111111 01000000
        // 00000000 00000000 00000000 01000000
        // 2 to the sixth power is 64

        // 00000000 00000000 00000000 00001100
        // & 
        // 11111111 11111111 11111111 11110100
        // 00000000 00000000 00000000 00000100
        // 2 squared = 4
        int d = b & -b;

        System.out.println("The results are " + c + " and " + d);
    }
}